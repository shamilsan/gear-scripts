async fn get_codes(node: &str, code_ids: &[&str]) -> Result<()> {
    let api = GearApi::init(WSAddress::new(node, 443)).await?;
    println!("Connected to the node\n");

    let mut listener = api.subscribe().await?;

    assert!(listener.blocks_running().await?);
    println!("Blocks are running\n");

    for id in code_ids {
        let code_id = hex::decode(id)?;
        let code = api
            .original_code_at(code_id.as_slice().into(), None)
            .await?;
        println!("Code {id} len: {}", code.len());
        fs::write(format!("codes/{id}.wasm"), code)?;
    }
    Ok(())
}

